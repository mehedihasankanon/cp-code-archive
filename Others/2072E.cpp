void Solve(ll test)
{
    ll k; cin >> k;
    vector<pair<ll,ll>> v;
    ll i = -100000, j = -100000;

    while(k > 0)
    {
        ll t = 0, m = 0;
        v.push_back({i,j});
        while(t <= k)
        {
            j++;
            v.push_back({i,j});
            m++;
            t += m;
            if(t > k)
            {
                v.pop_back();
                t -= m;
                break;
            }
        }
        k -= t;
        i++;
    }

    cout << v.size() << nl;
    for(auto [a,b] : v) cout << a << " " << b << nl;
    return;
}   
    
